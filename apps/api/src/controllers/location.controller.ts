// Create a new location
export async function createLocation(req: AuthRequest, res: Response) {
  try {
    const { village, city, taluka, district, state, pincode } = req.body;

    if (!state) {
      return res.status(400).json({ error: 'State is required' });
    }

    const location = await prisma.location.create({
      data: { village, city, taluka, district, state, pincode },
    });

    res.status(201).json({ location });
  } catch (error) {
    logger.error('Create location error:', error);
    res.status(500).json({ error: 'Failed to create location' });
  }
}

// Update a location
export async function updateLocation(req: AuthRequest, res: Response) {
  try {
    const { id } = req.params;
    const { village, city, taluka, district, state, pincode } = req.body;

    const existing = await prisma.location.findUnique({ where: { id } });
    if (!existing) {
      return res.status(404).json({ error: 'Location not found' });
    }

    const location = await prisma.location.update({
      where: { id },
      data: {
        ...(village !== undefined && { village }),
        ...(city !== undefined && { city }),
        ...(taluka !== undefined && { taluka }),
        ...(district !== undefined && { district }),
        ...(state !== undefined && { state }),
        ...(pincode !== undefined && { pincode }),
      },
    });

    res.json({ location });
  } catch (error) {
    logger.error('Update location error:', error);
    res.status(500).json({ error: 'Failed to update location' });
  }
}

// Delete a location
export async function deleteLocation(req: AuthRequest, res: Response) {
  try {
    const { id } = req.params;

    const existing = await prisma.location.findUnique({ where: { id } });
    if (!existing) {
      return res.status(404).json({ error: 'Location not found' });
    }

    // Check if location is referenced by any entity
    const [farmerCount, trialCount, customerCount, supplierCount] = await Promise.all([
      prisma.farmer.count({ where: { location_id: id } }),
      prisma.trial.count({ where: { location_id: id } }),
      prisma.customer.count({ where: { location_id: id } }),
      prisma.supplier.count({ where: { location_id: id } }),
    ]);

    const totalRefs = farmerCount + trialCount + customerCount + supplierCount;
    if (totalRefs > 0) {
      return res.status(400).json({
        error: `Cannot delete location: it is referenced by ${farmerCount} farmer(s), ${trialCount} trial(s), ${customerCount} customer(s), ${supplierCount} supplier(s)`,
      });
    }

    await prisma.location.delete({ where: { id } });
    res.json({ message: 'Location deleted successfully' });
  } catch (error) {
    logger.error('Delete location error:', error);
    res.status(500).json({ error: 'Failed to delete location' });
  }
}
